        students with necessary training and technical knowledge in various aspects of software for the IT industry. The course
        prepares skilled IT professionals who are able to meet the demands of the job market and the broader economy,
        particularly in the development of computer application systems for companies in Cambodia.
    </p>
    <p>
        Upon completion of this four year degree, students are proficient in:
    </p>
    <ul class="list-disc ml-10">
        <li>
            Designing and coding structured programs in commonly used computer languages;
        </li>
        <li>
            Performing computer programming and object-oriented programming;
        </li>
        <li>
            Utilizing database management systems
        </li>
        <li>
            Effectively developing network and Internet systems
        </li>
        <li>
            Applying structured and object-oriented development approaches in software development
        </li>
        <li>
            Assisting in formulating and developing Client-Server solutions and Client-Sever database systems
        </li>
        <li>
            Identifying the components in various computer systems configurations.
        </li>
    </ul>
    <p>
        Students who graduate from this course find employment in a wide range of private and public services and organizations.
        Graduates work in IT-related fields including client services, web development, network operations, program structuring,
        data structuring and object-oriented programming (OOP).
